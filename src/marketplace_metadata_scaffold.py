"""Scaffolder for ``.claude-plugin/marketplace-metadata.json`` (Gap 2 / #469).

A curated-marketplace maintainer should not hand-author the Agnes enrichment
file from a blank page. Everything that is *derivable* from the canonical
sources — ``marketplace.json``, each plugin's ``plugin.json``, and the
``SKILL.md`` / agent ``.md`` frontmatter — can be generated; the curator then
layers human-only content (cover photos, polished copy, sample interactions)
on top. The runtime parser lives in :mod:`src.marketplace_metadata`; this
module emits exactly the fields that parser recognises.

The hard part is the *merge*, not the derivation. A re-run must refresh what
the machine owns (a renamed skill, an edited description) without ever
clobbering what a human wrote. We classify every field:

  GEN   deterministically derived; regenerated each run.
          plugin : display_name
          skill  : display_name, invocation
          agent  : display_name, invocation
  DRAFT seeded from a canonical source as a starting point; treated exactly
        like GEN by the merge (so a human edit still wins).
          plugin : tagline        (first line of plugin.json description)
          skill  : when_to_use    (skill frontmatter ``description``)
          agent  : when_to_use    (agent frontmatter ``description``)
  KEEP  human-only; the scaffolder never writes these and passes existing
        values through untouched.
          cover_photo, video_url, category, description, use_cases,
          sample_interaction, doc_links

Merge contract (3-way; base = the value we last generated, tracked by a
content hash in a top-level ``_generated`` block):

  existing absent                          -> write derived   (fill)
  present, never generated by us           -> keep existing   (KEEP)
  present, hash == last generated          -> write derived   (regenerate)
  present, hash != last generated          -> keep existing   (human edit wins)

When a field we generated last run is no longer derivable (its upstream source
was deleted), it never appears in ``derived``, so a final cleanup pass handles
it — otherwise a stale machine value would linger and, having lost its
provenance, masquerade as human content on the next run:

  was ours, on-disk hash == last generated -> remove          (source gone)
  was ours, on-disk hash != last generated -> keep existing   (human edit wins)

``_generated`` is ignored by the runtime parser (it reads only ``plugins``),
so shipping it in the committed file is safe. It is what turns the old "keep
the enrichment file in sync with the plugins by hand" chore into "re-run the
scaffolder and read the diff" — and powers ``--check`` in CI.

This module deliberately has no ``app.`` dependency so it stays importable and
unit-testable on its own (mirrors the rule in
``src/store_guardrails/_frontmatter.py``).
"""

from __future__ import annotations

import copy
import hashlib
import json
import re
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path, PurePosixPath
from typing import Any, Dict, List, Optional, Tuple

from src.marketplace_metadata import (
    MARKETPLACE_METADATA_REL,
    read_marketplace_metadata,
)

GENERATOR_TAG = "agnes-marketplace-scaffold"
SCHEMA_VERSION = 1

# Mirrors ``src.marketplace.PLUGIN_MANIFEST_REL``. Re-declared here rather than
# imported because that module pulls in ``app.utils`` at import time, and this
# scaffolder must stay free of the app layer.
PLUGIN_MANIFEST_REL = Path(".claude-plugin") / "marketplace.json"

PLUGIN_JSON_REL = Path(".claude-plugin") / "plugin.json"

# Fields the merge owns. Everything else a curator puts in the file is KEEP and
# never touched. (Documented in the module docstring; duplicated here only as
# the set the report iterates over.)
_OWNED_STATUSES = frozenset({"generated", "regenerated", "unchanged"})

_FRONTMATTER_RE = re.compile(r"^---\s*\n(.*?)\n---", re.DOTALL)


class ScaffoldError(Exception):
    """Raised for unrecoverable input problems (missing / malformed
    ``marketplace.json``). The CLI turns this into a one-line error + exit 1.

    Note this is *stricter* than the runtime sync path
    (:func:`src.marketplace_metadata.read_marketplace_metadata`), which degrades
    to ``{}``. A curator running the scaffolder wants a loud failure, not a
    silently empty result.
    """


# --------------------------------------------------------------------------- #
# Frontmatter + string helpers
# --------------------------------------------------------------------------- #


def _parse_frontmatter(text: str) -> Dict[str, str]:
    """Parse a leading ``--- ... ---`` YAML-ish block into a flat dict.

    Mirrors :func:`src.marketplace_listing._parse_frontmatter` (skip blank and
    ``#`` comment lines, split each remaining line on the first ``:``, strip
    surrounding quotes). Kept local so the scaffolder doesn't have to import
    the heavier ``store_guardrails`` package just for this.
    """
    m = _FRONTMATTER_RE.match(text)
    if not m:
        return {}
    out: Dict[str, str] = {}
    for line in m.group(1).splitlines():
        if not line.strip() or line.lstrip().startswith("#"):
            continue
        if ":" in line:
            k, v = line.split(":", 1)
            out[k.strip()] = v.strip().strip('"').strip("'")
    return out


def humanize(slug: str) -> str:
    """Turn a slug into a Title-Cased display name.

    ``"gws-onboarding"`` -> ``"Gws Onboarding"``, ``"dept-howto"`` ->
    ``"Dept Howto"``. Only the first letter of each token is upper-cased so
    internal capitals survive (``"APIClient"`` stays ``"APIClient"``). This is
    a best-effort GEN value — a curator who wants "Google Workspace Onboarding"
    just edits it, and the merge then leaves their edit alone.
    """
    parts = re.split(r"[-_\s]+", slug.strip())
    return " ".join(p[:1].upper() + p[1:] for p in parts if p)


def _first_line(text: str) -> str:
    for line in (text or "").splitlines():
        s = line.strip()
        if s:
            return s
    return ""


def _field_hash(value: Any) -> str:
    """Stable short content hash of a JSON-able value (provenance tracking)."""
    canon = json.dumps(value, sort_keys=True, ensure_ascii=False)
    return hashlib.sha256(canon.encode("utf-8")).hexdigest()[:16]


def _utcnow_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


